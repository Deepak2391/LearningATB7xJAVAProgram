package Variable_Identifier_Operator;
 // unary operator --> only one operand is required
// Binary operator --> 2 operand are required
// Ternary operator --> 3 operand are required
public class Lab38_UnaryOperator {
    public static void main(String[] args) {

        int a = 65; // unary plus
        int b = -1; // Unary minus
        System.out.println(a);
        System.out.println(b);
        System.out.println(b+1);
        System.out.println(b+2);

    }
}

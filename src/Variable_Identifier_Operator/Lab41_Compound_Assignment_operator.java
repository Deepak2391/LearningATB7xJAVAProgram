package Variable_Identifier_Operator;
// Compound Assignment Operator--> +=,-=,/=,*=
public class Lab41_Compound_Assignment_operator {
    public static void main(String[] args) {
        //+=
        int a = 10;
        a+=10; // a =a+10
        a-=10; // a =a-10
        a/=10; // a =a/10
        a*=10; // a =a*10

    }
}

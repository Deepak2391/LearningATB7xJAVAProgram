package Variable_Identifier_Operator;

public class Lab26_Replacement {
    public static void main(String[] args) {
        String name = "Deepak";
        int age = 31;
        System.out.printf("Your name is --> %s", name); // Printf --> %s use for String
        System.out.printf("Your age is --> %d", age); // Printf --> %d Use for Int // Printf --> %f use for float And double.

    }
}
